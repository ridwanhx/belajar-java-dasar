public class DemoArray {
    public static void main(String[] args) {
        // inisiasi array nama dengan beberapa nilai
        String[] nama = { "Ridwan", "Alfia", "Hana" };
        // perulangan terhadap array (menggunakan foreach)
        for (String n : nama) {
            // tampilkan output di layar
            System.out.println(n);
        }
    }
}